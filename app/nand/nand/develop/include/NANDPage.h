#ifndef NANDPAGE_H
#define NANDPAGE_H

#include "ByteBuffer.h"
////////////////////////////////////////////////////////////////////////////////
//
//
//
////////////////////////////////////////////////////////////////////////////////
struct NANDPage
{
    utility::hex::ByteBuffer spare;
    utility::hex::ByteBuffer data;

    bool empty()     
    { 
        return spare.size() + data.size() == 0; 
    }

    bool blank() 
    {
        if (empty()) 
            return false;

        return  data.all_values_are(0xff) &&
               spare.all_values_are(0xff);
    }
};

////////////////////////////////////////////////////////////////////////////////
//
//
//
////////////////////////////////////////////////////////////////////////////////
#endif
